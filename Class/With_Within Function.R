#With ()
################################################################################
df <- data.frame(x=c(3, 5, 5, 7, 6, 10),
                 y=c(2, 2, 0, 5, 9, 4))
with(df, x*y)

################################################################################
#within ()
################################################################################
within(df, z <- x*y)
#multiply values in x and y and assign results to new data frame
df_new <- within(df, z <- x*y)
